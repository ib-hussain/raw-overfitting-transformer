# URDU STEMMER/LEMMATIZER (Rule-based, simplified from Assignment 1)
# ============================================================================
class UrduStemmer:
    """Rule-based Urdu stemmer"""
    
    # Common Urdu suffixes
    SUFFIXES = [
        'وں', 'وں', 'یں', 'وں', 'یں',
        'تے', 'تے', 'تی', 'تا', 'نا', 'نے', 'نی', 'ن', 'اں',
        'ئیں', 'ئے', 'ئے', 'ئ', 'یا', 'یہ', 'ی', 'ے', 'ہیں',
        'گے', 'گی', 'گا', 'کر', 'کی', 'کا', 'کو', 'کی', 'کے',
        'سے', 'پر', 'تک', 'بھر', 'سا', 'سی', 'سے', 'سا', 'سے',
        'والا', 'والی', 'والے', 'وں', 'کیا', 'گیا', 'جا', 'دے',
    ]
    
    # Sort suffixes by length (longest first)
    SUFFIXES.sort(key=len, reverse=True)
    
    def stem(self, word):
        """Return the stem of a word"""
        if len(word) < 3:
            return word
        
        for suffix in self.SUFFIXES:
            if word.endswith(suffix) and len(word) - len(suffix) >= 2:
                return word[:-len(suffix)]
        
        return word


