def load_sentences(filepath):
    """Load all sentences from cleaned.txt"""
    if DEBUG_MODE:
        print(f"[DEBUG]: Loading sentences from {filepath}")
    
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    sentences = []
    doc_pattern = r'\[\d+\]\s*\n'
    doc_texts = re.split(doc_pattern, content)
    
    for doc_text in doc_texts:
        if doc_text.strip():
            # Split by <SOS>...<EOS> to get sentences
            sent_pattern = r'<SOS>(.*?)<EOS>'
            matches = re.findall(sent_pattern, doc_text, re.DOTALL)
            for match in matches:
                sent = match.strip()
                if sent:
                    # Tokenize by whitespace
                    tokens = sent.split()
                    tokens = [t for t in tokens if t]
                    if tokens:
                        sentences.append(tokens)
    
    if DEBUG_MODE:
        print(f"[DEBUG]: Loaded {len(sentences)} sentences")
    
    return sentences


def load_metadata(filepath):
    """Load metadata.json for topic information"""
    if DEBUG_MODE:
        print(f"[DEBUG]: Loading metadata from {filepath}")
    
    with open(filepath, 'r', encoding='utf-8') as f:
        metadata = json.load(f)
    
    # Infer topics from titles
    topics = {}
    for doc_id, info in metadata.items():
        title = info.get('title', '')
        topic = infer_topic_from_title(title)
        
        # Map doc_id to topic
        doc_num = int(doc_id)
        topics[doc_num] = topic
    
    return topics


def infer_topic_from_title(title):
    """Infer topic category from title keywords"""
    if any(kw in title for kw in ['پاکستان', 'وزیر', 'حکومت', 'سیاست', 'عمران', 'نواز', 'زرداری', 'الیکشن']):
        return 'Politics'
    elif any(kw in title for kw in ['کرکٹ', 'کھیل', 'ورلڈ کپ', 'میچ', 'پلیئر', 'ٹیم']):
        return 'Sports'
    elif any(kw in title for kw in ['دہشت', 'حملہ', 'فوج', 'آپریشن', 'پولیس', 'قتل', 'خودکش']):
        return 'Security'
    elif any(kw in title for kw in ['معیشت', 'ڈالر', 'روپے', 'کاروبار', 'سٹاک', 'برآمد']):
        return 'Economy'
    elif any(kw in title for kw in ['امریکہ', 'انڈیا', 'چین', 'افغانستان', 'ایران', 'خارجہ']):
        return 'International'
    elif any(kw in title for kw in ['عدالت', 'مقدمہ', 'قانون', 'جج', 'سپریم']):
        return 'Legal'
    else:
        return 'General'


def get_document_topic_mapping(cleaned_file, metadata_file):
    """Map each sentence to its document topic"""
    with open(cleaned_file, 'r', encoding='utf-8') as f:
        content = f.read()
    
    topics = load_metadata(metadata_file)
    
    doc_pattern = r'\[(\d+)\]\s*\n'
    matches = list(re.finditer(doc_pattern, content))
    
    sent_to_topic = []
    current_doc = 0
    
    # Process each document
    for i, match in enumerate(matches):
        doc_id = int(match.group(1))
        topic = topics.get(doc_id, 'General')
        
        # Find all sentences in this document
        start = match.end()
        end = matches[i+1].start() if i+1 < len(matches) else len(content)
        doc_text = content[start:end]
        
        sent_pattern = r'<SOS>(.*?)<EOS>'
        sent_matches = re.findall(sent_pattern, doc_text, re.DOTALL)
        
        for _ in sent_matches:
            sent_to_topic.append(topic)
    
    return sent_to_topic


def select_sentences(sentences, sent_to_topic, num_sentences=500, min_per_topic=100):
    """
    Randomly select 500 sentences with at least 100 from 3 distinct topics
    """
    if DEBUG_MODE:
        print(f"[DEBUG]: Selecting {num_sentences} sentences")
    
    # Group sentences by topic
    topic_sentences = defaultdict(list)
    for i, sent in enumerate(sentences):
        if i < len(sent_to_topic):
            topic = sent_to_topic[i]
            topic_sentences[topic].append((i, sent))
    
    if DEBUG_MODE:
        print(f"[DEBUG]: Topic distribution in corpus:")
        for topic, sents in topic_sentences.items():
            print(f"[DEBUG]:   - {topic}: {len(sents)} sentences")
    
    # Select top 3 topics by frequency
    top_topics = sorted(topic_sentences.keys(), key=lambda t: len(topic_sentences[t]), reverse=True)[:3]
    
    if DEBUG_MODE:
        print(f"[DEBUG]: Selected topics: {top_topics}")
    
    selected_indices = set()
    selected_sentences = []
    
    # First, select min_per_topic from each of the 3 topics
    for topic in top_topics:
        available = topic_sentences[topic]
        if len(available) >= min_per_topic:
            selected = random.sample(available, min_per_topic)
        else:
            selected = available
        
        for idx, sent in selected:
            if idx not in selected_indices:
                selected_indices.add(idx)
                selected_sentences.append((idx, sent, topic))
    
    # Fill remaining slots randomly from all sentences
    remaining_needed = num_sentences - len(selected_sentences)
    if remaining_needed > 0:
        all_available = [(i, sent, sent_to_topic[i] if i < len(sent_to_topic) else 'General') 
                        for i, sent in enumerate(sentences) 
                        if i not in selected_indices]
        
        if len(all_available) >= remaining_needed:
            additional = random.sample(all_available, remaining_needed)
            selected_sentences.extend(additional)
    
    # Sort by original index
    selected_sentences.sort(key=lambda x: x[0])
    
    if DEBUG_MODE:
        print(f"[DEBUG]: Selected {len(selected_sentences)} sentences")
        topic_counts = defaultdict(int)
        for _, _, topic in selected_sentences:
            topic_counts[topic] += 1
        print(f"[DEBUG]: Selected topic distribution: {dict(topic_counts)}")
    
    return selected_sentences


def annotate_dataset(selected_sentences, pos_tagger, ner_tagger):
    """Annotate selected sentences with POS and NER tags"""
    annotated = []
    
    for idx, tokens, topic in selected_sentences:
        pos_tags = pos_tagger.tag_sentence(tokens)
        ner_tags = ner_tagger.tag_sentence(tokens)
        
        annotated.append({
            'sentence_id': idx,
            'topic': topic,
            'tokens': tokens,
            'pos_tags': pos_tags,
            'ner_tags': ner_tags
        })
    
    return annotated


def split_dataset(annotated, train_ratio=0.7, val_ratio=0.15, test_ratio=0.15):
    """
    Split dataset into train/val/test stratified by topic
    """
    # Group by topic
    topic_groups = defaultdict(list)
    for item in annotated:
        topic_groups[item['topic']].append(item)
    
    train_data, val_data, test_data = [], [], []
    
    for topic, items in topic_groups.items():
        n = len(items)
        n_train = int(n * train_ratio)
        n_val = int(n * val_ratio)
        
        # Shuffle
        random.shuffle(items)
        
        train_data.extend(items[:n_train])
        val_data.extend(items[n_train:n_train + n_val])
        test_data.extend(items[n_train + n_val:])
    
    # Shuffle each split
    random.shuffle(train_data)
    random.shuffle(val_data)
    random.shuffle(test_data)
    
    return train_data, val_data, test_data


def report_class_distribution(dataset, name):
    """Report class label distribution for POS and NER"""
    pos_counts = defaultdict(int)
    ner_counts = defaultdict(int)
    total_tokens = 0
    
    for item in dataset:
        for pos in item['pos_tags']:
            pos_counts[pos] += 1
        for ner in item['ner_tags']:
            ner_counts[ner] += 1
        total_tokens += len(item['tokens'])
    
    print(f"\n{name} Set - Class Distribution:")
    print("=" * 50)
    print(f"Total tokens: {total_tokens}")
    print(f"Total sentences: {len(dataset)}")
    
    print("\nPOS Tag Distribution:")
    print("-" * 30)
    for tag in POS_TAGS + ['UNK']:
        count = pos_counts.get(tag, 0)
        pct = count / total_tokens * 100 if total_tokens > 0 else 0
        print(f"  {tag:8s}: {count:6d} ({pct:5.2f}%)")
    
    print("\nNER Tag Distribution:")
    print("-" * 30)
    for tag in NER_TAGS:
        count = ner_counts.get(tag, 0)
        pct = count / total_tokens * 100 if total_tokens > 0 else 0
        print(f"  {tag:8s}: {count:6d} ({pct:5.2f}%)")
    
    return dict(pos_counts), dict(ner_counts)


