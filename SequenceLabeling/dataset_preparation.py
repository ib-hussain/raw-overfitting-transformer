    """NER tagger using gazetteer lookup with BIO scheme"""
    
    def __init__(self, gazetteer):
        self.gazetteer = gazetteer
        
        # Build reverse lookup
        self.word_to_ner = {}
        for ner_type, words in gazetteer.items():
            for word in words:
                if word not in self.word_to_ner:
                    self.word_to_ner[word] = []
                self.word_to_ner[word].append(ner_type)
        
        if DEBUG_MODE:
            print(f"[DEBUG]: NER Tagger initialized with {len(self.word_to_ner)} entities")
    
    def tag_sentence(self, tokens):
        """Tag a sentence using BIO scheme"""
        tags = []
        i = 0
        
        while i < len(tokens):
            token = tokens[i]
            ner_type = self._get_ner_type(token)
            
            if ner_type != 'O':
                # Beginning of an entity
                tags.append(f'B-{ner_type}')
                i += 1
                
                # Check for continuation (multi-word entities)
                while i < len(tokens):
                    next_token = tokens[i]
                    next_type = self._get_ner_type(next_token)
                    
                    # Check if next token is part of same entity type
                    if next_type == ner_type:
                        tags.append(f'I-{ner_type}')
                        i += 1
                    else:
                        break
            else:
                tags.append('O')
                i += 1
        
        return tags
    
    def _get_ner_type(self, token):
        """Get NER type for a single token"""
        if token in self.word_to_ner:
            types = self.word_to_ner[token]
            # Prefer PER over other types if multiple
            if 'PER' in types:
                return 'PER'
            elif 'LOC' in types:
                return 'LOC'
            elif 'ORG' in types:
                return 'ORG'
            elif 'MISC' in types:
                return 'MISC'
            return types[0]
        return 'O'


