# POS TAGGER (Rule-based)
# ============================================================================
class UrduPOSTagger:
    """Rule-based POS tagger for Urdu"""
    
    def __init__(self, lexicon, stemmer):
        self.lexicon = lexicon
        self.stemmer = stemmer
        
        # Build reverse lookup for faster tagging
        self.word_to_pos = {}
        for pos, words in lexicon.items():
            for word in words:
                if word not in self.word_to_pos:
                    self.word_to_pos[word] = []
                self.word_to_pos[word].append(pos)
        
        if DEBUG_MODE:
            print(f"[DEBUG]: POS Tagger initialized with {len(self.word_to_pos)} words")
    
    def tag(self, token):
        """Tag a single token with its POS tag"""
        # Check punctuation first
        if token in POS_LEXICON.get('PUNC', set()) or token in ['.', ',', '?', '!', '۔', '،', '؛', ':']:
            return 'PUNC'
        
        # Check numbers
        if token in POS_LEXICON.get('NUM', set()) or token.isdigit() or token == '<NUM>':
            return 'NUM'
        
        # Check lexicon
        if token in self.word_to_pos:
            # Return first (most common) POS tag
            return self.word_to_pos[token][0]
        
        # Rule-based fallback
        stem = self.stemmer.stem(token)
        
        # Check stem in lexicon
        if stem in self.word_to_pos:
            return self.word_to_pos[stem][0]
        
        # Heuristic rules based on suffixes
        if any(token.endswith(suf) for suf in ['نا', 'تے', 'تی', 'تا', 'یں', 'و', 'ے']):
            return 'VERB'
        elif any(token.endswith(suf) for suf in ['وں', 'یں', 'اں', 'ے', 'ات']):
            return 'NOUN'
        elif token.endswith('سے') or token.endswith('کر'):
            return 'ADV'
        elif token.endswith('والا') or token.endswith('والی') or token.endswith('والے'):
            return 'ADJ'
        
        # Default to UNK
        return 'UNK'
    
    def tag_sentence(self, tokens):
        """Tag all tokens in a sentence"""
        return [self.tag(token) for token in tokens]


